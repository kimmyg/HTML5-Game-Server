=begin

the game has two states: standard and finalization

in standard, a turn consists of one of:
discard
meld
challenge

in finalization, a turn consists of one of:
pass
meld
challenge

the game ends when everyone passes in a row

once the option is chosen

discard:
tap one of the cards in hand to discard, message is sent, draw automatically
cancel button

meld:
tap two of cards in hand plus discard.
cancel button

challenge:
tap one of cards in hand plus discard and tap card in hand.
- each constrains the other, reflect this.
cancel button (even after one of the cards has been selected.)

=end
